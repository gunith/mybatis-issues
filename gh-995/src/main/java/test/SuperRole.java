package test;

public class SuperRole {

	private Integer id;
	private String name;
	private SuperRoleStatus status;
	private Type type;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}


	public SuperRoleStatus getStatus() {
		return status;
	}

	public void setStatus(SuperRoleStatus status) {
		this.status = status;
	}

	public Type getType() {
		return type;
	}

	public void setType(Type type) {
		this.type = type;
	}

	@Override
	public String toString() {
		return "SuperRole [id=" + id + ", name=" + name + ", status=" + status + ", type=" + type + "]";
	}


}
